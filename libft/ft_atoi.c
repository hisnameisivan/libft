#include "libft.h"

int		ft_atoi(const char *str)
{
	int					sign;
	unsigned long long	result;

	sign = 1;
	result = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		if (result > FT_LONG_MAX && sign == 1)
			return (-1);
		if (result > FT_LONG_MAX + 1 && sign == -1)
			return (0);
		str++;
	}
	return ((int)(sign * result));
}
