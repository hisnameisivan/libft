#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*temp;
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(s1);
	if (!(temp = (char *)malloc(sizeof(*s1) * (len + 1))))
		return (NULL);
	while (s1[i] != '\0')
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
